$(document).ready(function () {
  $(window).scroll(function () {
    // sticky navbar on scroll script
    if (this.scrollY > 20) {
      $(".navbar").addClass("sticky");
    } else {
      $(".navbar").removeClass("sticky");
    }

    // scroll-up button show/hide script
    if (this.scrollY > 500) {
      $(".scroll-up-btn").addClass("show");
    } else {
      $(".scroll-up-btn").removeClass("show");
    }
  });

  // slide-up script
  $(".scroll-up-btn").click(function () {
    $("html").animate({ scrollTop: 0 });
    // removing smooth scroll on slide-up button click
    $("html").css("scrollBehavior", "auto");
  });

  $(".navbar .menu li a").click(function () {
    // applying again smooth scroll on menu items click
    $("html").css("scrollBehavior", "smooth");
  });

  // toggle menu/navbar script
  $(".menu-btn").click(function () {
    $(".navbar .menu").toggleClass("active");
    $(".menu-btn i").toggleClass("active");
  });

  // typing text animation script
  var typed = new Typed(".typing", {
    strings: ["CSE Student", "Coder", "Web Developer", "Network Analist"],
    typeSpeed: 100,
    backSpeed: 60,
    loop: true,
  });
  const scriptURL = 'https://docs.google.com/forms/u/0/d/e/1FAIpQLScDXmLorMlsg0xZ9xhAwff04WF1q7VRo7zOoPD7JGBNcWlhkw/formResponse'
  const form = document.forms['google-sheet']

  form.addEventListener('submit', e => {
      e.preventDefault()
      fetch(scriptURL, { method: 'POST', body: new FormData(form)})
          .then(response => console.log("Thanks for Contacting us..! We Will Contact You Soon...",response))
          .catch(error => console.error('Error!', error.message))
      document.getElementById("myForm").reset();
  });
});
